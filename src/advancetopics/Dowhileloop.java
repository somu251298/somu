package advancetopics;

public class Dowhileloop
{
	public static void main ( String[] args)
	{
		int a=1;
		do {
			System.out.println(a);
			a++;
		}
		while(a<=25);
		System.out.println("----------decreament----------");
		int b=25;
		do
		{
			System.out.println(b);
			b--;
		}
		while(b>=1);
	}
}
